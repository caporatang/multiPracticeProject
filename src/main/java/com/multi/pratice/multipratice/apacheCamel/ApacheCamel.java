public class ApacheCamel {
    // 아파치 카멜...

    // Concise Application Message Exchange Laguage :  일반 어플리케이션에 내장 가능한 경량 프레임워크
    // Camel은 처리하고자 하는 데이터 타입에 어떤 가정도 하지 않으며 높은 레벨의 추상화를 제공함 -> 시스템에서 사용하는 데이터 타입이나 프로토콜에 상관없이 API를 사용해 상호 작용할 수 있다.

    // 프레임워크 내부에 라우터 엔진, 프로세서, 컴포넌트, 메시징 시스템을 포함, 어플리케이션 내부 - 외부 간의 인터페이스 역할을 한다.
    // 다른 솔루션을 연동하기 위한 어댑터는 제공 되지만,  오픈 소스인 만큼 기업에서 많이 사용되는 솔루션의 Component는 부족하다.
    // Camel은 EAI(기업 응용프로그램 통합 솔루션) 같은 솔루션이 아닌 이를 개발하기 위한 프레임워크이다.

    // 어플리케이션 개발자는 내부-외부 간 인터페이스 기술을 모두 알아야하는 번거로움이 있고 많은 어려움이 있다.
    // Camel을 사용하는 경우 인터페이스 역할을 Camel이 해줌으로 인해서 비즈니스 어플리케이션 개발자는 Camel 개발자는 인터페이스를 하려는 외부 시스템 개발자와 소통하게 된다.

    // Camel은 서로 다른 기술, 다른 프로토콜을 쓰는 어플리케이션들을 통합 할 때 쓰는 것이 좋으며, 하나 혹은 두 가지 또는 너무 많은 어플리케이션을 통합 할 때는 사용하지 않는 것이 좋다.





}